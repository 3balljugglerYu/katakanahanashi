  }

  void _handleNext(
    BuildContext context,
    WidgetRef ref,
    GameViewModel gameViewModel,
    bool isSubscribed,
  ) {
    final currentWord = gameViewModel.currentWord;
    final navigator = Navigator.of(context, rootNavigator: true);

    // Ë©ï‰æ°„ÉÄ„Ç§„Ç¢„É≠„Ç∞„ÇíË°®Á§∫
    final gameState = ref.read(gameViewModelProvider);
    final isLastQuestion = gameState.currentQuestionIndex == 9;

    showDialog<void>(
      context: context,
      barrierDismissible: false,
      builder: (context) => RatingDialog(
        word: currentWord,
        onSubmit: (rating) async {
          // „É≠„Éº„Éá„Ç£„É≥„Ç∞„ÉÄ„Ç§„Ç¢„É≠„Ç∞„ÇíË°®Á§∫
          showDialog(
            context: context,
            barrierDismissible: false,
            builder: (context) => const Center(
              child: Card(
                child: Padding(
                  padding: EdgeInsets.all(20),
                  child: Column(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      CircularProgressIndicator(),
                      SizedBox(height: 16),
                      Text('Ë©ï‰æ°„ÇíÈÄÅ‰ø°‰∏≠...'),
                    ],
                  ),
                ),
              ),
            ),
          );

          // Ë©ï‰æ°„ÇíÈÄÅ‰ø°
          final result = await gameViewModel.submitRating(rating);

          // „É≠„Éº„Éá„Ç£„É≥„Ç∞„ÉÄ„Ç§„Ç¢„É≠„Ç∞„ÇíÈñâ„Åò„Çã
          if (navigator.mounted) {
            navigator.pop();
          }

          // 10ÂïèÁõÆ„ÅÆÂ†¥Âêà„ÅØÂ∫ÉÂëäÊ°àÂÜÖ„Éö„Éº„Ç∏„Å´ÈÅ∑Áßª
          if (isLastQuestion) {
            _navigateToCongratulationsPage(
              navigator,
              gameViewModel,
              isSubscribed,
            );
          } else {
            _proceedToNext(context, gameViewModel);
          }
        },
      ),
    );
  }

  /// ÁîªÈù¢„Çµ„Ç§„Ç∫„Å´Âøú„Åò„Åü„Éú„Éà„É†„Çπ„Éö„Éº„Ç∑„É≥„Ç∞„ÇíË®àÁÆó
  double _getResponsiveBottomSpacing(BuildContext context) {
    final screenHeight = MediaQuery.of(context).size.height;
    final screenWidth = MediaQuery.of(context).size.width;

    // „Çø„Éñ„É¨„ÉÉ„ÉàÂà§ÂÆöÔºàÂπÖ„Åå800px‰ª•‰∏ä„Åæ„Åü„ÅØÈ´ò„Åï„Åå1000px‰ª•‰∏äÔºâ
    final isTablet = screenWidth >= 800 || screenHeight >= 1000;

    if (isTablet) {
      // „Çø„Éñ„É¨„ÉÉ„Éà„ÅÆÂ†¥ÂêàÔºöÊúÄÂ∞èÈôê„ÅÆ„Çπ„Éö„Éº„Ç∑„É≥„Ç∞
      return screenHeight * 0.02; // 2%
