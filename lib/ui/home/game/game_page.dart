  void _showInterstitialAd(BuildContext context, VoidCallback onAdClosed) {
    InterstitialAd.load(
      adUnitId: AdService.interstitialAdUnitId,
      request: const AdRequest(),
      adLoadCallback: InterstitialAdLoadCallback(
        onAdLoaded: (InterstitialAd ad) {
          // Â∫ÉÂëä„ÅÆ„Ç≥„Éº„É´„Éê„ÉÉ„ÇØ„ÇíÂÖà„Å´Ë®≠ÂÆö
          ad.fullScreenContentCallback = FullScreenContentCallback(
            onAdDismissedFullScreenContent: (InterstitialAd ad) {
              print("Â∫ÉÂëä„ÅåÁµÇ‰∫Ü„Åï„Çå„Åæ„Åó„Åü");
              ad.dispose();
              // Â∫ÉÂëäÁµÇ‰∫ÜÊôÇ„ÅØ„Ç≥„Éº„É´„Éê„ÉÉ„ÇØ„ÇíÂëº„Å∞„Å™„ÅÑÔºàÊó¢„Å´„Éà„ÉÉ„ÉóÁîªÈù¢„Å´ÈÅ∑ÁßªÊ∏à„ÅøÔºâ
            },
            onAdFailedToShowFullScreenContent:
                (InterstitialAd ad, AdError error) {
                  print("Â∫ÉÂëäË°®Á§∫„Å´Â§±Êïó„Åó„Åæ„Åó„Åü: ${error.message}");
                  ad.dispose();
                  onAdClosed();
                },
            onAdShowedFullScreenContent: (InterstitialAd ad) {
              print("Â∫ÉÂëä„ÅåË°®Á§∫„Åï„Çå„Åæ„Åó„Åü");
              // Â∞ë„ÅóÈÅÖÂª∂„Åó„Å¶„Åã„Çâ„Éà„ÉÉ„ÉóÁîªÈù¢„Å´ÈÅ∑Áßª
              Future.delayed(const Duration(milliseconds: 100), () {
                onAdClosed();
              });
            },
          );
          // „Ç≥„Éº„É´„Éê„ÉÉ„ÇØË®≠ÂÆöÂæå„Å´Â∫ÉÂëä„ÇíË°®Á§∫
          ad.show();
        },
        onAdFailedToLoad: (LoadAdError error) {
          print("Â∫ÉÂëäË™≠„ÅøËæº„Åø„Å´Â§±Êïó„Åó„Åæ„Åó„Åü: ${error.message}");
          // Â∫ÉÂëäË™≠„ÅøËæº„ÅøÂ§±ÊïóÊôÇ„ÅØ„Ç≥„Éº„É´„Éê„ÉÉ„ÇØ„ÇíÂÆüË°å
          onAdClosed();
        },
      ),
    );
  }

  void _proceedToNext(BuildContext context, GameViewModel gameViewModel) {
    print(
      "_proceedToNext called. isLastQuestion: ${gameViewModel.isLastQuestion}",
    );

    // 10ÂïèÁõÆ„ÅÆÂ†¥Âêà„ÅÆ„Åø„Éà„ÉÉ„ÉóÁîªÈù¢„Å´Êàª„Çã
    if (gameViewModel.isLastQuestion) {
