      // „Çπ„Éû„Éº„Éà„Éï„Ç©„É≥„ÅÆÂ†¥ÂêàÔºöÂæìÊù•„ÅÆ„Çπ„Éö„Éº„Ç∑„É≥„Ç∞
      return screenHeight * 0.12; // 12%
    }
  }
