    // if (AppConfig.isDebugMode) {
    //   AdService.logAdInfo(); // „Éá„Éê„ÉÉ„Ç∞ÊÉÖÂ†±Âá∫Âäõ
    // }
    InterstitialAd.load(
      adUnitId: AdService.interstitialAdUnitId,
      request: const AdRequest(),
      adLoadCallback: InterstitialAdLoadCallback(
        onAdLoaded: (InterstitialAd ad) {
          print("Â∫ÉÂëä„ÅÆ‰∫ãÂâçË™≠„ÅøËæº„Åø„ÅåÂÆå‰∫Ü„Åó„Åæ„Åó„Åü");
          _preloadedAd = ad;
        },
        onAdFailedToLoad: (LoadAdError error) {
          print("Â∫ÉÂëä„ÅÆ‰∫ãÂâçË™≠„ÅøËæº„Åø„Å´Â§±Êïó„Åó„Åæ„Åó„Åü: ${error.message}");
          _preloadedAd = null;
        },
