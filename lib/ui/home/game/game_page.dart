  static void _preloadInterstitialAd({required bool shouldLoad}) {
    if (!shouldLoad) {
      _preloadedAd = null;
      return;
    }

    print("Â∫ÉÂëä„ÅÆ‰∫ãÂâçË™≠„ÅøËæº„Åø„ÇíÈñãÂßã„Åó„Åæ„Åô");
