      // „Åì„Çå„Å´„Çà„ÇäÊúÄÂæå„ÅÆ„ÉØ„Éº„Éâ„ÅåSharedPreferences„Å´ÁôªÈå≤„Åï„Çå„Çã
      gameViewModel.nextQuestion();

      // „Ç≤„Éº„É†ÁµÇ‰∫ÜÂæå„ÅÆ„É™„Çª„ÉÉ„ÉàÂà§ÂÆö„Å®ÂÆüË°åÔºàÈùûÂêåÊúü„ÅßÂÆüË°åÔºâ
